public class assignment_operator{
    public static void main(String[]args){
        int a = 10;
        a += 5; // a = a + 5
        a -= 3; // a = a - 3
        a *= 2; // a = a * 2
        a /= 4; // a = a / 4
        a %= 3; // a = a % 3
        System.out.println("The final value of a is: " + a);
        
    }
}