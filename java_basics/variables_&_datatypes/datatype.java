// Java is a typed language: --//> means we have to specify the data type of a variable while declaring it.

public class datatype{
    public static void main(String[]args){
        int a = 5;              //size:- 4 bytes
        boolean out = true;     //size:- 1 bytes
        char grade = 'A';       //size:- 2 bytes
        float b = 5.67f;        //size:- 4 bytes
        double c = 67.484;      //size:- 8 bytes
        long d = 4265436;       //size:- 4 bytes
        byte z = 22;            //size:- 1 bytes
        String name = "Aniroodh";
        System.out.println("Integer value: " + a);
        System.out.println("Boolean value: " + out);
        System.out.println("Character value: " + grade);
        System.out.println("Float value: " + b);
        System.out.println("Double value: " + c);
        System.out.println("Long value: " + d);
        System.out.println("String value: " + name);
        System.out.println("Byte value: " + z);

        
    }
}